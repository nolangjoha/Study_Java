package Study_ch03;

public class Ex3_7_usual_arithmetic_conversion {

	//[산술변환]
	public static void main(String args[]) {
		
		System.out.println(5/2); 		// int형으로 소수점은 버려짐
		System.out.println(5/(float)2); // 산술변환 규칙으로 float형으로 자동 형변환
		System.out.println((float)5/2); // 산술변환 규칙으로 float형으로 자동 형변환
		
		
		
		
		
	}
	
	
}
