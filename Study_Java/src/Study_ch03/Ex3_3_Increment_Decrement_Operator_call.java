package Study_ch03;

public class Ex3_3_Increment_Decrement_Operator_call {
	
	// [메서드 호출에서 증감연산자]
	public static void main(String argsp[]) {

		int i=5, j=5;
		
		System.out.println(i++);  // 출력은 5, 이후 i값은 6이다.
		
		System.out.println(++j);  // 출력 전에 계산돼서 j값은 6이다.
		
		System.out.println("i = " + i + ", j = " +j);
		
	}
	
	
	
}
