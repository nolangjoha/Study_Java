package Study_ch02;

public class Ex2_7_String_concat {
	
	public static void main(String args[]) {
		String name = "Ja" + "va";
		String str = name + "8.0";
		
		System.out.println(name);
		System.out.println(str);
		
		System.out.println(7 + " ");
		System.out.println(" " + 7);
		System.out.println(7 + "");
		System.out.println("" + 7);
		System.out.println("" + "");
		System.out.println(7 + 7 + "");
		System.out.println("" + 7 + 7);
		
		
		
	}
	
	
	
}
